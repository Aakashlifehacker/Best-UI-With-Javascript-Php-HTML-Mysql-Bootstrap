$('#cssmenu li.active').addClass('open').children('ul').show();
	$('#cssmenu li.has-sub>a').on('click', function(){
		$(this).removeAttr('href');
		var element = $(this).parent('li');
		if (element.hasClass('open')) {
			element.removeClass('open');
			element.find('li').removeClass('open');
			element.find('ul').slideUp(200);
		}
		else {
			element.addClass('open');
			element.children('ul').slideDown(200);
			element.siblings('li').children('ul').slideUp(200);
			element.siblings('li').removeClass('open');
			element.siblings('li').find('li').removeClass('open');
			element.siblings('li').find('ul').slideUp(200);
		}
	});

});
})(jQuery);

    </script>
	<script>
			
	$(window).scroll(function(){
		var window_top = $(window).scrollTop() + 1; 
		if (window_top > 800) {
			$('.rd_menu_section, .rp_mobail_menu_main_wrapper').addClass('menu_fixed animated fadeInDown');
		} else {
			$('.rd_menu_section, .rp_mobail_menu_main_wrapper').removeClass('menu_fixed animated fadeInDown');
		}
	});
	</script>
	
	
	<script>
			
		$(window).scroll(function() {
			if ($(this).scrollTop() >= 100) {       
				$('#return-to-top').fadeIn(200);   
			} else {
				$('#return-to-top').fadeOut(200);  
			}
		});
		$('#return-to-top').on('click', function() {     
			$('body,html').animate({
				scrollTop : 0                
			}, 500);
		});

	</script>
	
	
	<script>
	
				//Add One Page nav
	if($('.scroll-nav').length) {
		$('.scroll-nav ul.navbar-nav').onePageNav();
	}
	
	//Hide Bootstrap Menu On Click over Mobile View
	$('.scroll-nav ul.navbar-nav > li > a').on('click', function(){
		var windowWidth = $(window).width();
		if (windowWidth <= 767) {
			$('.nav-outer .navbar-toggle').trigger( "click" );
		}
	});
	
	
	// Scroll to a Specific Div
	if($('.scroll-to-target').length){
		$(".scroll-to-target").on('click', function() {
			var target = $(this).attr('data-target');
		   // animate
		   $('html, body').animate({
			   scrollTop: $(target).offset().top
			 }, 1000);

		});
	}
		
	</script>
	
<script type="text/javascript">
    $(document).ready(function(){
        $('#insert').click(function(){
            var image_name = $('#image').val();
            if (image_name=='') 
            {
                alert("Please select Image");
                return false;
            }
            else
            {
                var extension = $('#image').val().split('.').pop().toLowerCase();
                if (jQuery.inArray(extension,['gif','png','jpg','jpeg'])==-1) 
                {
                    alert('Invalid Image File');
                    $('#image').val('');
                    return false;
                }
            }
        })
    });
</script>
   

